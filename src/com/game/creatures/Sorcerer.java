package com.game.creatures;
public class Sorcerer extends Hero {

    private double dexterityInc;
    private double agilityInc;

    public double getDexterityInc() {
        return dexterityInc;
    }

    public void setDexterityInc(double dexterityInc) {
        this.dexterityInc = dexterityInc;
    }

    public double getAgilityInc() {
        return agilityInc;
    }

    public void setAgilityInc(double agilityInc) {
        this.agilityInc = agilityInc;
    }
}
